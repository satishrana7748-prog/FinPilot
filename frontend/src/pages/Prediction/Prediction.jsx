import { useState } from "react";
import jsPDF from "jspdf";
function Prediction() {
  const [goal, setGoal] = useState("");
  const [amount, setAmount] = useState("");
  const [duration, setDuration] = useState("");

  const [loading, setLoading] = useState(false);

  const [result, setResult] = useState(null);

  const generatePrediction = async () => {
    if (!goal || !amount || !duration) {
      alert("Please fill all fields");
      return;
    }

    setLoading(true);

    try {
      const response = await fetch("http://127.0.0.1:8000/predict", {
        method: "POST",

        headers: {
          "Content-Type": "application/json",
        },

        body: JSON.stringify({
          goal,

          amount: Number(amount),

          duration: Number(duration),
        }),
      });

      const data = await response.json();

      setTimeout(() => {
        setResult(data);

        setLoading(false);
      }, 1800);
    } catch {
      alert("Backend Connection Failed");

      setLoading(false);
    }
  };

  const downloadReport = () => {
    if (!result) {
      alert("Generate AI Recommendation first.");
      return;
    }

    const doc = new jsPDF();

    doc.setFontSize(22);
    doc.text("FinPilot AI Recommendation Report", 20, 20);

    doc.setFontSize(14);

    doc.text(`Recommendation : ${result.recommendation}`, 20, 45);

    doc.text(`Expected Return : ${result.expectedReturn}`, 20, 60);

    doc.text(`Confidence : ${result.confidence}`, 20, 75);

    doc.text(`Risk Score : Medium`, 20, 90);

    doc.text(`Investment Horizon : Long Term`, 20, 105);

    doc.text(
      "Generated by FinPilot AI Multi-Asset Recommendation System",
      20,
      140,
    );

    doc.save("FinPilot_AI_Report.pdf");
  };
  return (
    <div className="p-10">
      <h1 className="text-4xl font-black mb-10">
        AI Investment Recommendation
      </h1>

      <div className="grid lg:grid-cols-2 gap-10">
        {/* Left */}

        <div className="bg-white rounded-3xl shadow-xl p-8">
          <h2 className="text-2xl font-bold mb-6">Investment Details</h2>

          <div className="space-y-5">
            <select
              value={goal}
              onChange={(e) => setGoal(e.target.value)}
              className="w-full border rounded-xl p-4"
            >
              <option value="">Select Goal</option>

              <option>Wealth Creation</option>

              <option>Retirement</option>

              <option>Education</option>

              <option>Emergency Fund</option>
            </select>

            <input
              type="number"
              placeholder="Investment Amount"
              value={amount}
              onChange={(e) => setAmount(e.target.value)}
              className="w-full border rounded-xl p-4"
            />

            <input
              type="number"
              placeholder="Investment Duration"
              value={duration}
              onChange={(e) => setDuration(e.target.value)}
              className="w-full border rounded-xl p-4"
            />

            <button
              onClick={generatePrediction}
              className="w-full bg-gradient-to-r from-cyan-600 to-blue-700 text-white py-4 rounded-xl font-bold hover:scale-[1.02] transition"
            >
              Generate AI Recommendation
            </button>
            <button
              onClick={downloadReport}
              className="w-full mt-4 bg-green-600 hover:bg-green-700 text-white py-4 rounded-xl font-bold transition"
            >
              Download AI Report (PDF)
            </button>
          </div>
        </div>

        {/* Right */}

        <div className="bg-white rounded-3xl shadow-xl p-8">
          {loading ? (
            <div className="flex flex-col items-center justify-center h-full">
              <div className="w-20 h-20 border-8 border-cyan-300 border-t-cyan-700 rounded-full animate-spin"></div>

              <h2 className="text-2xl font-bold mt-8">
                AI is analyzing market...
              </h2>

              <p className="text-gray-500 mt-4">Please wait...</p>
            </div>
          ) : result ? (
            <div className="space-y-6">
              <div className="bg-green-50 rounded-2xl p-6">
                <h2 className="font-bold text-xl">Recommendation</h2>

                <h1 className="text-5xl font-black text-green-600 mt-4">
                  {result.recommendation}
                </h1>
              </div>

              <div className="bg-orange-50 rounded-2xl p-6">
                <h2 className="font-bold">Risk Level</h2>

                <div className="mt-4">
                  <span className="px-5 py-2 rounded-full bg-orange-500 text-white font-bold">
                    Moderate Risk
                  </span>
                </div>
              </div>
              <div className="bg-blue-50 rounded-2xl p-6">
                <h2 className="font-bold">Expected Return</h2>

                <h1 className="text-3xl font-bold mt-3">
                  {result.expectedReturn}
                </h1>
              </div>

              <div className="bg-purple-50 rounded-2xl p-6">
                <h2 className="font-bold">AI Confidence</h2>

                <h1 className="text-3xl font-bold mt-3">{result.confidence}</h1>

                <div className="mt-5">
                  <div className="h-3 bg-gray-300 rounded-full overflow-hidden">
                    <div
                      className="h-full bg-gradient-to-r from-cyan-500 to-blue-700"
                      style={{ width: result.confidence }}
                    ></div>
                  </div>
                </div>
              </div>
              <div className="bg-slate-100 rounded-2xl p-6">
                <h2 className="text-xl font-bold">AI Explanation</h2>

                <p className="mt-4 leading-8 text-gray-600">
                  FinPilot AI analyzed historical prices, moving averages, RSI,
                  volatility, momentum, and market sentiment. Based on the
                  current market conditions, this investment provides a balanced
                  opportunity for medium and long-term growth.
                </p>
              </div>

              <div className="bg-slate-100 rounded-2xl p-6">
                <h2 className="text-xl font-bold">Suggested Allocation</h2>

                <div className="grid grid-cols-2 gap-4 mt-5">
                  <div className="bg-white rounded-xl p-5 shadow">
                    <h3 className="font-bold">Equity</h3>

                    <h1 className="text-3xl font-black text-cyan-600">60%</h1>
                  </div>

                  <div className="bg-white rounded-xl p-5 shadow">
                    <h3 className="font-bold">Gold</h3>

                    <h1 className="text-3xl font-black text-yellow-500">20%</h1>
                  </div>

                  <div className="bg-white rounded-xl p-5 shadow">
                    <h3 className="font-bold">Crypto</h3>

                    <h1 className="text-3xl font-black text-purple-600">10%</h1>
                  </div>

                  <div className="bg-white rounded-xl p-5 shadow">
                    <h3 className="font-bold">Cash</h3>

                    <h1 className="text-3xl font-black text-green-600">10%</h1>
                  </div>
                </div>
              </div>
            </div>
          ) : (
            <div className="flex flex-col justify-center items-center h-full">
              <h2 className="text-3xl font-bold">Waiting for Prediction</h2>

              <p className="text-gray-500 mt-4">
                Enter your investment details and click Generate.
              </p>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

export default Prediction;
